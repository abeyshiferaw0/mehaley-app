                    getPendingSelfIntent(context, CHANGE_LANG)
                )

            }

            appWidgetManager.updateAppWidget(widgetId, views)
        }
    }

    override fun onReceive(context: Context?, intent: Intent?) {
        super.onReceive(context, intent)

        val appWidgetManager = AppWidgetManager.getInstance(context)
        val remoteViews: RemoteViews =
            RemoteViews(context!!.packageName, R.layout.verse_widget_layout)
        val watchWidget: ComponentName =
            ComponentName(context, VersesWidgetProvider::class.java)


        if (intent != null) {
            if (CHANGE_VERSE == intent.action) {
                setVerse(remoteViews,context)
                appWidgetManager.updateAppWidget(watchWidget, remoteViews)
            }
            if (CHANGE_LANG == intent.action) {
                Log.d(TAG, "onReceive: "+getLang(context))
                if (getLang(context) == AM) {
                    setLang(context,EN)
                } else {
                    setLang(context,AM)
                }
                Log.d(TAG, "onReceive: "+getLang(context))
                ///SET VERSE TEXTS
                setVerse(remoteViews,context)
                //SET VIEWS TEXT
                setViewTexts(remoteViews,context)

                appWidgetManager.updateAppWidget(watchWidget, remoteViews)
            }
        }
    }


    private fun setVerse(remoteViews: RemoteViews,context:Context) {

        ///GET RANDOM INDEX
        val randomIndex = nextInt(versesListEn.size)
        ///SET VERSE AND BY TEXT
        if (getLang(context) == AM) {
            remoteViews.setTextViewText(R.id.txt_verse, versesListAm[randomIndex])
            remoteViews.setTextViewText(R.id.txt_verse_by, versesByListAm[randomIndex])
        } else {
            remoteViews.setTextViewText(R.id.txt_verse, versesListEn[randomIndex])
            remoteViews.setTextViewText(R.id.txt_verse_by, versesByListEn[randomIndex])
        }
    }

    private fun setViewTexts(remoteViews: RemoteViews,context:Context) {
        if (getLang(context) == AM) {
            remoteViews.setTextViewText(R.id.txt_open_app, OPEN_APP_AM)
            remoteViews.setTextViewText(R.id.btn_change_lang, SELECTED_LAN_EN)
            remoteViews.setTextViewText(R.id.txt_verses_form_mehaley, VERSES_FROM_MEHALEY_AM)

        } else {
            remoteViews.setTextViewText(R.id.txt_open_app, OPEN_APP_EN)
            remoteViews.setTextViewText(R.id.btn_change_lang, SELECTED_LAN_AM)
            remoteViews.setTextViewText(R.id.txt_verses_form_mehaley, VERSES_FROM_MEHALEY_EN)

        }
    }

    private fun getLang(context:Context):String{
        shared = context.getSharedPreferences(VERSE_WIDGET_VALUES , Context.MODE_PRIVATE)
        var lang = shared.getString(CURRENT_LANG,AM)
        if(lang==null)return AM
        return lang
    }

    private fun setLang(context:Context,lan:String){
        shared = context.getSharedPreferences(VERSE_WIDGET_VALUES , Context.MODE_PRIVATE)
        shared.edit().putString(CURRENT_LANG,lan).apply()
    }

    private fun getPendingSelfIntent(context: Context?, action: String?): PendingIntent? {
        val intent = Intent(context, javaClass)
        intent.action = action
        return PendingIntent.getBroadcast(context, 0, intent, 0)
    }
